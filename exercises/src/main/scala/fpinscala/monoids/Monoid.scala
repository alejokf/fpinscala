  sealed trait WC
  case class Stub(chars: String) extends WC
  case class Part(lStub: String, words: Int, rStub: String) extends WC

  val wcMonoid: Monoid[WC] = new Monoid[WC] {
    override def op(a1: WC, a2: WC): WC = (a1, a2) match {
      // to combine 2 Stubs we simply concatenate both chars in another Stub
      case (Stub(c1), Stub(c2)) => Stub(c1 + c2)
      // to combine 1 Stub and 1 Part we concatenate the chars of the Stub to the left or right Stub of the Part
      case (Stub(c), Part(l, w, r)) => Part(c + l, w, r)
      case (Part(l, w, r), Stub(c)) => Part(l, w, r + c)
      // to combine two parts, we check if the left Stub of part1 and right Stub of part2 form a word, and add 1 to the word count
      case (Part(l1, w1, r1), Part(l2, w2, r2)) => Part(l1, w1 + w2 + (if ((l2 + r1).isEmpty) 0 else 1), r2)
    }

    override def zero: WC = Stub("")
  }

  def count(s: String): Int = {
    def toWC(c: Char): WC =
      if (c.isWhitespace)
        Part("", 0, "")
      else
        Stub(c.toString)

    def countWord(s: String) = s.length.min(1)

    foldMapV(s.toIndexedSeq, wcMonoid)(toWC) match {
      case Stub(c) => countWord(c)
      case Part(l, w, r) => countWord(l) + w + countWord(r)
    }
  }

